class Solution
{
  public:
    int minOperation(int n)
    {
        //code here.
        int k=n,cnt=0;
        while(k>0){
            if(k%2==1){
                k=k-1;
                cnt++;
            }
           k=k/2;
            cnt++;
        }
        return cnt-1;
    }
};

