class Solution
{
    public:
    struct Node* makeUnion(struct Node* head1, struct Node* head2)
    {
        // code here
        map<int,int>m;
        Node* temp=head1;
        while(temp!=NULL){
            m[temp->data]++;
            temp=temp->next;
        }
        temp=head2;
        while(temp!=NULL){
            m[temp->data]++;
            temp=temp->next;
        }
       temp=new Node(0);
        Node * head=temp;
        Node* prev=NULL;
        for(auto x:m){
            temp->data=x.first;
            if(temp->next==NULL){
                Node * ne=new Node(0);
                temp->next=ne;
            }
            prev=temp;
            temp=temp->next;
        }
        prev->next=NULL;
        return head;
    }
};


